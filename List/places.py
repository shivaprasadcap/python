places= ['Paris', 'Rajkot', 'Goa', 'London', 'Hong Kong']
print(places)

# Display temporary sorted list 
print(sorted(places))
print(places)

# Reverse the list and store 
places.reverse()
print(places)

# Revert back to original list
places.reverse()
print(places)

#Sort in alphabatical order
places.sort()
print(places)

# Reverse alphabatical order
places.sort(reverse=True)
print(places)